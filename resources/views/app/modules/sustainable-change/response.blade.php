                        <p>These are some of my personal habits that have got me where I am today.”</em></p>

                        <p><b class="personal-habits-title">I start my year right</b>
                        At the beginning of each year I write out a very detailed goals statement. I write it as though I have achieved everything on the list already. It covers my family, career, relationships and how I want to show up.</p>

                        <p><b class="personal-habits-title">I look at the whole year, and draw out a typical week.</b>
                        I am a visual person, so I buy a large full year calendar and mark everything on it that is unmovable. I then draw out what a typical week will look like for the year, blocking in everything set in stone: work commitments, kids school times, exercise, etc. This is then transferred to my Google calendar.</p>

                        <p><b class="personal-habits-title">Digital calendars</b>
                        If you aren’t digital you need to be!</p>

                        <p><b class="personal-habits-title">Delegation</b>
                        Early on in my career I made the decision to delegate as soon as I had the income to do so. My top priorities are my family and my work, everything else that can be, gets delegated.</p>

                        <p><b class="personal-habits-title">Looking after me, so I can look after others</b>
                        I allocate time for my head, my heart and my health. I can only be of service to those around me when I am healthy and happy. The best gift I can give my children is to show them what it looks like to have it all.
                        </p>

                        <p><b>Deciding in advance what to say Yes to, and what to say No to</b>
                        If you have more than three priorities you have none!</p>
