    <div class="content-block">
        <p class="center"><b>Belief School is a fully automated online program</b> you can access from any device. Eight interactive modules born out of thousands of face-to-face hours, each created with the purpose of giving you a framework to build confidence, solve problems and create the life you want.</p>

        <p class="center"><b>Each module has approx. 15 minutes theory,</b> a short video, an optional audio coaching session (if you want greater depth) and an interactive online exercise.</p>

        <p class="center"><b>Each module is a stand-alone learning experience</b>—giving you a single principle to work with, an exercise that is very revealing and a lesson to take into your daily life. However, all eight modules are connected and build upon one another, to create more than their individual parts.</p>

        <p class="center"><b>As you progress through the program, you are building your live, dynamic Dashboard</b>—collecting data, either from you, or from your world. This is regularly updated as a rich resource and a reflection of your progress.</p>

        <p class="center"><b>Upon completion, your Dashboard becomes your beautiful Belief School Manifesto.</b> A collection of all that you have created through this program, presented back to you as a downloadable product. It is a tool to use in times ahead and a reminder of the great value you bring to your world.</p>

        <p class="center"><b>Belief School is a community.</b> Whether you are connecting with people in our Forum, or through our Closed Facebook group, the Belief School Team and our past and present Belief Schoolers, are there to support you, cheer you on and shed a tear if needed.</p>
